    private void btnRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRutaActionPerformed
        
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos XML", "xml");
        JFileChooser abrirArchivo = new JFileChooser();
        abrirArchivo.setFileFilter(filtro);
        abrirArchivo.setDialogTitle("Escoge el XML de un CFDI");
                
        int opcion = abrirArchivo.showOpenDialog(this);
        
        if(opcion == JFileChooser.APPROVE_OPTION){
            
            txtRuta.setText(abrirArchivo.getSelectedFile().getPath());
            
        }
        
    }//GEN-LAST:event_btnRutaActionPerformed


