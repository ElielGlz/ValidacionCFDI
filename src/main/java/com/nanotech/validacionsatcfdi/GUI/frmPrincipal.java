    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        
                
        if(jtpOpciones.getSelectedIndex() != 0){
            
            if(txtRFCEmisor.getText().isEmpty() || txtRFCReceptor.getText().isEmpty() && txtMonto.getValue().toString().isEmpty() ||
                txtUUID1.getValue().toString().isEmpty() || txtUUID2.getValue().toString().isEmpty() || txtUUID3.getValue().toString().isEmpty() ||
                txtUUID4.getValue().toString().isEmpty() || txtUUID5.getValue().toString().isEmpty()){
            
                JOptionPane.showMessageDialog(this, "Se deben llenar todos los campos", "Mensaje - Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            
        }
            
            consultarPorParametros();
            return;
            
        }
        
        if(txtRuta.getText().isEmpty()){ JOptionPane.showMessageDialog(this, "Selecciona un Archivo para procesar", "Mensaje - Advertencia", JOptionPane.WARNING_MESSAGE); return;}
        
        consultarPorXML();
        
    }//GEN-LAST:event_btnValidarActionPerformed

