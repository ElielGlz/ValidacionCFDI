    public void consultarPorParametros(){
        
        CFDI objCfdi = new CFDI();
        objCfdi.setRFCemisor(txtRFCEmisor.getText());
        objCfdi.setRFCreceptor(txtRFCReceptor.getText());
        objCfdi.setMonto(txtMonto.getValue().toString());
        objCfdi.setUUID(txtUUID1.getValue() + "-" + txtUUID2.getValue() + "-" + 
                        txtUUID3.getValue() + "-" + txtUUID4.getValue().toString() + "-" + txtUUID5.getValue().toString());
        
        
        switch(objCfdi.getExiste()){
            
            case "N - 602: Comprobante no encontrado.":
                
                lblResultado.setText("El CFDI no se pudo localizar");
                
                break;
               
            case "S - Comprobante obtenido satisfactoriamente.":
                
                lblResultado.setText("CFDI encontrado satisfactoriamente \n" +"Su Estado es: " + objCfdi.getEstatus());
                
                break;
                
            case "N - 601: La expresión impresa proporcionada no es válida.":
                
                lblResultado.setText("Los datos ingresados no son válidos");
                
                break;
                
            default:
                
                lblResultado.setText("Error al consultar el CFDI");
                
                break;
            
        }
        
    }
    
